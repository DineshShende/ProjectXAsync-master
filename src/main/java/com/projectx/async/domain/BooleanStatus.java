package com.projectx.async.domain;

public class BooleanStatus {
	
	private Boolean status;

	
	
	public BooleanStatus() {
		super();
	}

	public BooleanStatus(Boolean status) {
		super();
		this.status = status;
	}

	public Boolean getStatus() {
		return status;
	}

	public void setStatus(Boolean status) {
		this.status = status;
	}
	
	

}
