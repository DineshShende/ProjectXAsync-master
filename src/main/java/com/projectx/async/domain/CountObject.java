package com.projectx.async.domain;

public class CountObject {

	private Integer count;

	
	
	
	public CountObject() {
		super();
	}




	public CountObject(Integer count) {
		super();
		this.count = count;
	}




	public Integer getCount() {
		return count;
	}




	public void setCount(Integer count) {
		this.count = count;
	}
	
	
	
}
